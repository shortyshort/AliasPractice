public class Num
{
    public  class Num
    {
        private int value;

        public Num (int value)
        {
            this.value = value;
        }//end constructor

        public void setValue(int value)
        {
            this.value = value;
        }//ends setter

        public String toString ()
        {
            return "Value is " + value;
        }
    }//end class Num

}
